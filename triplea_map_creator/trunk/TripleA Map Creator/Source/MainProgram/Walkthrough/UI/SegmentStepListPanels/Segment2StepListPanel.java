    private Color GetConstantColorFromStepIndex(int buttonIndex, int fsegment, int step, int stepIndex)
    {
        if(fsegment > 1)
        {
            if(step == buttonIndex)
                return Constants.HighlightedSegmentBackColor;
            else
                return Constants.FinishedSegmentBackColor;
        }
        else
        {
            if(step == buttonIndex)
                return Constants.HighlightedSegmentBackColor;
            else if(stepIndex <= buttonIndex)
                return Constants.DefaultButtonBackColor;
            else
                return Constants.FinishedSegmentBackColor;
        }
    }
